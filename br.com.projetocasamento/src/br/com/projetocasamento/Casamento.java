package br.com.projetocasamento;

import java.sql.Date;

public class Casamento {

	private String localC;
	private String localF;
	private String dataC;
	private String horaC;
	
	public String getLocalC() {
		return localC;
	}
	public void setLocalC(String localC) {
		this.localC = localC;
	}
	public String getLocalF() {
		return localF;
	}
	public void setLocalF(String localF) {
		this.localF = localF;
	}
	public String getDataC() {
		return dataC;
	}
	public void setDataC(String dataC) {
		this.dataC = dataC;
	}
	public String getHoraC() {
		return horaC;
	}
	public void setHoraC(String horaC) {
		this.horaC = horaC;
	}
	
	
	
}
